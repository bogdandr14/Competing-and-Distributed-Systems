package TaskNumber4;
/**
 * Row and column number at which demons or potions are located
 */
public class Position {
	/**
	 * x coordinate
	 */
	public int x;
	/**
	 * y coordinate
	 */
	public int y;

	/**
	 * Constructor. Initializes fields
	 * @param x row coordinate
	 * @param y column coordinate
	 */
	public Position(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
}
